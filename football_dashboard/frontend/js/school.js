/**
 * Load and display all matches
 */
async function loadMatches() {
    const container = document.getElementById('matchesTableBody');
    if (!container) return;
    
    try {
        const response = await fetch(`${API_BASE}/matches`, {
            headers: getAuthHeaders()
        });
        const data = await response.json();
        
        if (!data.matches || data.matches.length === 0) {
            container.innerHTML = '<tr><td colspan="6" class="empty-state">No matches found</td></tr>';
            return;
        }
        
        container.innerHTML = data.matches.map(match => `
            <tr>
                <td>${match.match_date ? new Date(match.match_date).toLocaleDateString() : 'TBD'}</td>
                <td>${escapeHtml(match.opponent || 'TBD')}</td>
                <td>${escapeHtml(match.venue || 'TBD')}</td>
                <td>${match.home_score !== null ? match.home_score : '-'} - ${match.away_score !== null ? match.away_score : '-'}</td>
                <td><span class="status-badge status-${match.status || 'scheduled'}">${match.status || 'scheduled'}</span></td>
                <td>
                    <button class="btn btn-sm btn-secondary" onclick="editMatch(${match.id})">
                        <i class="fas fa-edit"></i>
                    </button>
                    <button class="btn btn-sm btn-danger" onclick="deleteMatch(${match.id})">
                        <i class="fas fa-trash"></i>
                    </button>
                </td>
            </tr>
        `).join('');
        
    } catch (error) {
        console.error('Error:', error);
        container.innerHTML = '<tr><td colspan="6" class="error">Error loading matches</td></tr>';
    }
}

/**
 * Show the add match modal
 */
function showAddMatchModal() {
    document.getElementById('matchModalTitle').textContent = 'Add New Match';
    document.getElementById('matchForm').reset();
    document.getElementById('matchId').value = '';
    document.getElementById('matchModal').classList.add('active');
}

/**
 * Close the match modal
 */
function closeMatchModal() {
    document.getElementById('matchModal').classList.remove('active');
}

/**
 * Edit an existing match
 */
async function editMatch(matchId) {
    try {
        const response = await fetch(`${API_BASE}/matches/${matchId}`, {
            headers: getAuthHeaders()
        });
        const data = await response.json();
        
        if (data.match) {
            const match = data.match;
            document.getElementById('matchModalTitle').textContent = 'Edit Match';
            document.getElementById('matchId').value = match.id;
            document.getElementById('matchDate').value = match.match_date ? match.match_date.slice(0, 16) : '';
            document.getElementById('matchOpponent').value = match.opponent || '';
            document.getElementById('matchVenue').value = match.venue || '';
            document.getElementById('matchStatus').value = match.status || 'scheduled';
            document.getElementById('homeScore').value = match.home_score !== null ? match.home_score : '';
            document.getElementById('awayScore').value = match.away_score !== null ? match.away_score : '';
            document.getElementById('matchNotes').value = match.notes || '';
            document.getElementById('matchModal').classList.add('active');
        }
    } catch (error) {
        console.error('Error:', error);
        showToast('Error loading match details', 'error');
    }
}

/**
 * Save a match (create or update)
 */
async function saveMatch(e) {
    e.preventDefault();
    
    const matchId = document.getElementById('matchId').value;
    const data = {
        match_date: document.getElementById('matchDate').value,
        opponent: document.getElementById('matchOpponent').value,
        venue: document.getElementById('matchVenue').value,
        status: document.getElementById('matchStatus').value,
        home_score: document.getElementById('homeScore').value ? parseInt(document.getElementById('homeScore').value) : null,
        away_score: document.getElementById('awayScore').value ? parseInt(document.getElementById('awayScore').value) : null,
        notes: document.getElementById('matchNotes').value || null
    };
    
    try {
        const url = matchId ? `${API_BASE}/matches/${matchId}` : `${API_BASE}/matches`;
        const method = matchId ? 'PUT' : 'POST';
        
        const response = await fetch(url, {
            method: method,
            headers: getAuthHeaders(),
            body: JSON.stringify(data)
        });
        
        const result = await response.json();
        
        if (response.ok) {
            showToast(matchId ? 'Match updated successfully!' : 'Match added successfully!', 'success');
            closeMatchModal();
            loadMatches();
        } else {
            showToast(result.error || 'Error saving match', 'error');
        }
    } catch (error) {
        console.error('Error:', error);
        showToast('Error saving match', 'error');
    }
}

/**
 * Delete a match
 */
async function deleteMatch(matchId) {
    if (!confirm('Are you sure you want to delete this match?')) {
        return;
    }
    
    try {
        const response = await fetch(`${API_BASE}/matches/${matchId}`, {
            method: 'DELETE',
            headers: getAuthHeaders()
        });
        
        if (response.ok) {
            showToast('Match deleted successfully!', 'success');
            loadMatches();
        } else {
            const data = await response.json();
            showToast(data.error || 'Error deleting match', 'error');
        }
    } catch (error) {
        console.error('Error:', error);
        showToast('Error deleting match', 'error');
    }
}

/**
 * Search/filter matches
 */
function searchMatches() {
    const searchTerm = document.getElementById('matchSearch')?.value?.toLowerCase() || '';
    const statusFilter = document.getElementById('filterMatchStatus')?.value || '';
    
    const rows = document.querySelectorAll('#matchesTableBody tr');
    rows.forEach(row => {
        const text = row.textContent.toLowerCase();
        const status = row.querySelector('.status-badge')?.textContent?.toLowerCase() || '';
        
        const matchesSearch = text.includes(searchTerm);
        const matchesStatus = !statusFilter || status.includes(statusFilter);
        
        row.style.display = matchesSearch && matchesStatus ? '' : 'none';
    });
}

