/**
 * Save a new player
 * @param {Event} e - Form submit event
 */
async function savePlayer(e) {
    e.preventDefault();
    
    // Get team_id from current user with validation
    let teamId = currentUser?.entity_id;
    
    // Validate team_id - must be a positive integer
    if (!teamId || typeof teamId !== 'number' || teamId <= 0 || !Number.isInteger(teamId)) {
        console.warn('Invalid team_id, prompting user or using session default');
        // Use a session-based default or prompt - for now use null to let API handle it
        teamId = null;
    }
    
    // Parse height to integer (remove 'cm' suffix if present)
    let heightValue = document.getElementById('playerHeight').value;
    let heightCm = null;
    if (heightValue) {
        heightCm = parseInt(heightValue.replace(/[^0-9]/g, '')) || null;
    }
    
    // Note: district, sector, cell, village fields exist in the form but aren't stored 
    // in the database - they're sent for future use but won't be persisted currently
    const district = document.getElementById('playerDistrict').value || null;
    const sector = document.getElementById('playerSector').value || null;
    const cell = document.getElementById('playerCell').value || null;
    const village = document.getElementById('playerVillage').value || null;
    
    // Note: Registration number is auto-generated by the backend, not user-provided
    // The regNumber field in the form is ignored by the backend
    
    const data = {
        name: document.getElementById('playerName').value,
        dob: document.getElementById('playerDob').value,
        nationality: document.getElementById('playerNationality').value,
        position: document.getElementById('playerPosition').value,
        // Map team_id to school_id for school users
        school_id: teamId,
        // Height from form (parsed to integer)
        height_cm: heightCm,
        jersey_number: null
    };
    
    // Log the data being sent for debugging
    console.log('Sending player data to API:', data);

    try {
        const response = await fetch(`${API_BASE}/players`, {
            method: 'POST',
            headers: getAuthHeaders(),
            body: JSON.stringify(data)
        });
        
        if (response.ok) {
            const data = await response.json();
            showToast(`Player added successfully! Registration: ${data.registration_number || 'N/A'}`, 'success');
            document.getElementById('playerForm').reset();
            loadDashboardStats(); // Refresh stats
        } else {
            const errorData = await response.json();
            showToast(errorData.error || 'Error adding player', 'error');
        }
    } catch (error) {
        console.error('Error:', error);
        showToast('Error adding player', 'error');
    }
}

// Export to window for onclick handlers
window.savePlayer = savePlayer;

