// Current stream ID for live streaming
let currentStreamId = null;

/**
 * Initialize school dashboard when DOM is ready
 */
document.addEventListener('DOMContentLoaded', () => {
    // Check authentication first
    checkAuth();
    
    // Initialize mobile menu
    initMobileMenu();
    
    // Load initial dashboard data
    loadDashboardStats();
    
    // Set up auto-refresh for live data (every 30 seconds)
    setInterval(() => {
        loadDashboardStats();
    }, 30000);
});

/**
 * Show a specific section
 * @param {string} sectionId - The ID of the section to show
 */
function showSection(sectionId) {
    // Hide all sections
    document.querySelectorAll('.section').forEach(s => s.classList.remove('active'));
    
    // Show selected section
    const section = document.getElementById(sectionId);
    if (section) {
        section.classList.add('active');
    }
    
    // Load data for specific sections
    if (sectionId === 'statistics') {
        loadPlayerStats();
    } else if (sectionId === 'ml-training') {
        loadTrainingJobs();
    } else if (sectionId === 'live-match') {
        loadStreams();
    } else if (sectionId === 'matches') {
        loadMatches();
    }
}

// Export showSection to window for onclick handlers
window.showSection = showSection;

/**
 * Load dashboard statistics
 */
async function loadDashboardStats() {
    try {
        // Load total players count
        const playersResponse = await fetch(`${API_BASE}/players`, {
            headers: getAuthHeaders()
        });
        if (playersResponse.ok) {
            const playersData = await playersResponse.json();
            updateElement('totalPlayers', playersData.players?.length || 0);
        }
        
        // Load live streams count
        const streamsResponse = await fetch(`${API_BASE}/live-stream/list`, {
            headers: getAuthHeaders()
        });
        if (streamsResponse.ok) {
            const streamsData = await streamsResponse.json();
            const activeStreams = streamsData.streams?.filter(s => s.status === 'active').length || 0;
            updateElement('liveMatches', activeStreams);
        }
        
        // Load top players for "Top Talent" count
        const topPlayersResponse = await fetch(`${API_BASE}/ai/top-players?limit=100`, {
            headers: getAuthHeaders()
        });
        if (topPlayersResponse.ok) {
            const topData = await topPlayersResponse.json();
            updateElement('topTalent', topData.top_players?.length || 0);
        }
    } catch (error) {
        console.error('Error loading dashboard stats:', error);
    }
}

/**
 * Helper to safely update element text
 */
function updateElement(id, value) {
    const element = document.getElementById(id);
    if (element) {
        element.textContent = value;
    }
}

