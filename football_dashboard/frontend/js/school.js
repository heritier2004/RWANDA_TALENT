/**
 * Load and display all streams
 */
async function loadStreams() {
    const container = document.getElementById('streamsList');
    if (!container) return;
    
    try {
        const response = await fetch(`${API_BASE}/live-stream/list`, {
            headers: getAuthHeaders()
        });
        const data = await response.json();
        
        if (!data.streams || data.streams.length === 0) {
            container.innerHTML = `
                <div class="streams-empty">
                    <i class="fas fa-video-slash"></i>
                    <p>No active streams. Click "Start New Stream" to begin.</p>
                </div>
            `;
            return;
        }
        
        container.innerHTML = data.streams.map(stream => `
            <div class="stream-list-item">
                <div class="stream-info-left">
                    <div class="stream-status-indicator ${stream.status}"></div>
                    <div class="stream-details">
                        <h4>${stream.stream_name || 'Unnamed Stream'}</h4>
                        <p>${stream.status === 'active' ? '🔴 Live' : '⚪ Inactive'} | Created: ${stream.created_at ? new Date(stream.created_at).toLocaleDateString() : 'N/A'}</p>
                    </div>
                </div>
                <div class="stream-actions">
                    ${stream.status !== 'active' 
                        ? `<button class="btn-success" onclick="selectStream(${stream.id})">
                                <i class="fas fa-play"></i> Start
                           </button>`
                        : `<button class="btn-danger" onclick="selectAndStopStream(${stream.id})">
                                <i class="fas fa-stop"></i> Stop
                           </button>`
                    }
                </div>
            </div>
        `).join('');
        
    } catch (error) {
        console.error('Error:', error);
        container.innerHTML = `
            <div class="streams-empty">
                <i class="fas fa-exclamation-triangle"></i>
                <p>Error loading streams. Please try again.</p>
            </div>
        `;
    }
}

/**
 * Select a stream to manage
 */
function selectStream(streamId) {
    currentStreamId = streamId;
    // Show stream info for selected stream
    const streamInfo = document.getElementById('streamInfo');
    if (streamInfo) {
        streamInfo.style.display = 'block';
        document.getElementById('rtmpUrl').value = 'Select stream to see RTMP URL';
        document.getElementById('watchUrl').value = 'Select stream to see Watch URL';
        document.getElementById('streamKey').value = 'Select stream to see Key';
    }
    showToast('Stream selected', 'success');
}

/**
 * Select and stop a stream
 */
async function selectAndStopStream(streamId) {
    currentStreamId = streamId;
    await stopStream();
    loadStreams();
}

// Export additional stream functions
window.selectStream = selectStream;
window.selectAndStopStream = selectAndStopStream;

// Export stream functions to window
